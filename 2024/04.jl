# ╔═╡ ec8d4f5e-1651-480f-8393-d0e1ada4c594
is_mas(x) = join(x) in ("MAS", "SAM")

# ╔═╡ 17b76137-1638-4f44-a9e6-93120f907ff2
function partB(d)
	g=d |> IOBuffer |> readlines |> stack 
	(m,n)=size(g)
	#vis=fill(false,size(grid))
    sum = 0
    for i in 2:m-1 # can't fit in corners, so ignore
        for j in 2:n-1
			# now diags
			sum += is_mas([g[i+k,j+k] for k in -1:+1]) && is_mas([g[i-k,j+k] for k in -1:+1])
		end
	end

#	^^^ I suppose I could rewrite the above in some cool Julia interator...
	sum
end

# ╔═╡ c142c198-efc5-4fe2-98c4-5ddf0a530b18
function partBJuliaHipster(d)
	g=d |> IOBuffer |> readlines |> stack 
	(m,n)=size(g)	
#	^^^ I suppose I could rewrite the above in some cool Julia interator...
	[is_mas([g[i+k,j+k] for k in -1:+1]) && is_mas([g[i-k,j+k] for k in -1:+1]) for i in 2:m-1 for j in 2:n-1] |> count

end

