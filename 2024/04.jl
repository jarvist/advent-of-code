is_xmas(x) = join(x) in ("XMAS", "SAMX")

# ╔═╡ 9bdbf2da-9b3b-46c8-a6c9-20c965541b3c
function partA(d)
	g=d |> IOBuffer |> readlines |> stack 
	(m,n)=size(g)
	#vis=fill(false,size(grid))
    sum = 0
    for i in 1:m
        for j in 1:n
			# is_xmas deals with reversals, so let's horizontal and vertical
			sum += j<=(n-3) && is_xmas(g[i, j:(j + 3)])
			sum += i<=(m-3) && is_xmas(g[i:(i+3), j])			
			# now diags
			sum += j<=(n-3) && i<=(m-3) && is_xmas([g[i+k,j+k] for k in 0:3])
			sum += j<=(n-3) && i>3 && is_xmas([g[i-k,j+k] for k in 0:3])

		end
	end
	sum
end

